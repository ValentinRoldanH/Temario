function Sumar(){
    let numero1 = document.getElementById("num1").value;
    let numero2 = document.getElementById("num2").value;
    alert(parseInt(numero1) + parseInt(numero2));
}

function Restar(){
    let numero1 = document.getElementById("num1").value;
    let numero2 = document.getElementById("num2").value;
    alert(parseInt(numero1) - parseInt(numero2));
}

function Multiplicar(){
    let numero1 = document.getElementById("num1").value;
    let numero2 = document.getElementById("num2").value;
    alert(parseInt(numero1) * parseInt(numero2));
}

function Dividir(){
    let numero1 = document.getElementById("num1").value;
    let numero2 = document.getElementById("num2").value;
    alert(parseFloat(numero1) / parseFloat(numero2));
}



